pA <- c(0.75, 0.05, 0.05, 0.15)
pB <- c(0.61, 0.19, 0.19, 0.01)

Fx1x2 <- function(x1, x2, p) {
  p[1] * (1 - exp(-x1 / 10)) * (1 - exp(-x2 / 10)) + 
    p[2] * (1 - exp(-x1 / 20)) * (1 - exp(-x2 / 10)) + 
    p[3] * (1 - exp(-x1 / 10)) * (1 - exp(-x2 / 20)) + 
    p[4] * (1 - exp(-x1 / 20)) * (1 - exp(-x2 / 20))
}

# a -----------------------------------------------------------------------

Fx1x2(20, 10000, pA)
Fx1x2(10000, 20, pA)
Fx1x2(20, 10000, pB)
Fx1x2(10000, 20, pB)

# b -----------------------------------------------------------------------

Fx1x2(20, 20, pA)



















